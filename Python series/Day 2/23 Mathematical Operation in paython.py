import math

# Define variables
a = 10
b = 20

# Basic arithmetic
sum_result = a + b
diff_result = b - a
prod_result = a * b
div_result = b / a
mod_result = b % a
exp_result = a ** 2

# Using math module
sqrt_result = math.sqrt(a)
log_result = math.log(a)
sin_result = math.sin(math.pi / 2)
cos_result = math.cos(0)

# Print results
print("Sum:", sum_result)
print("Difference:", diff_result)
print("Product:", prod_result)
print("Division:", div_result)
print("Modulus:", mod_result)
print("Exponentiation:", exp_result)
print("Square Root:", sqrt_result)
print("Natural Log:", log_result)
print("Sine (90 degrees):", sin_result)
print("Cosine (0 degrees):", cos_result)

