# Numeric Types
a = 10          # int
b = 3.14        # float
c = 1 + 2j      # complex

# Sequence Types
s = "Hello, World!"   # str
l = [1, 2, 3, 4]      # list
t = (1, 2, 3, 4)      # tuple

# Mapping Type
d = {"name": "Alice", "age": 25}  # dict

# Set Types
set1 = {1, 2, 3}          # set
set2 = frozenset([1, 2, 3])  # frozenset

# Boolean Type
flag = True   # bool

# Binary Types
b = b"hello"              # bytes
ba = bytearray(b"hello")  # bytearray
mv = memoryview(b"hello") # memoryview
num=123_336_552
